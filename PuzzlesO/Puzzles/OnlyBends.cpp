{
    using namespace puzzles::OnlyBends;
    solve_puzzle<puz_game, puz_state, puz_solver_astar<puz_state>>(
        "Puzzles/OnlyBends.xml", "Puzzles/OnlyBends.txt", solution_format::GOAL_STATE_ONLY);
}
